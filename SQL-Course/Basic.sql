-- FILTERING REVIEW
SELECT
	*
FROM
	GRAMMY_WINNERS
WHERE
	YEAR BETWEEN 1990 AND 2000
	AND CATEGORY LIKE '%A%'
	AND WINNER = TRUE;

-- ORDER BY
SELECT
	*
FROM
	GRAMMY_WINNERS
WHERE
	ARTIST LIKE 'A%'
	OR ARTIST LIKE 'B%'
	AND YEAR > 2020
ORDER BY
	YEAR DESC;

