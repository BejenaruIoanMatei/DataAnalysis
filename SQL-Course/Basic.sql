SELECT
	TABLE_NAME
FROM
	INFORMATION_SCHEMA.TABLES
WHERE
	TABLE_SCHEMA = 'public';

CREATE VIEW GRAMMY_WINNERS_VIEW AS
SELECT
	*
FROM
	GRAMMY_WINNERS
LIMIT
	100;

SELECT
	*
FROM
	GRAMMY_WINNERS
LIMIT
	5;

-- SELECT 
SELECT
	SONG_OR_ALBUM
FROM
	GRAMMY_WINNERS
WHERE
	ARTIST = 'Beyoncé';

-- WHERE
SELECT
	YEAR,
	ARTIST
FROM
	GRAMMY_WINNERS
WHERE
	WINNER = 'true'
	AND ARTIST = 'Beyoncé';

-- AND/OR
SELECT
	ARTIST
FROM
	GRAMMY_WINNERS
WHERE
	YEAR < 2023
	AND WINNER = 'false'
	AND ARTIST IS NOT NULL;

-- BETWEEN
SELECT
	YEAR,
	ARTIST
FROM
	GRAMMY_WINNERS
WHERE
	YEAR BETWEEN 2000 AND 2010
	AND WINNER = 'TRUE';

-- SQL IN
SELECT
	YEAR,
	ARTIST,
	SONG_OR_ALBUM
FROM
	GRAMMY_WINNERS
WHERE
	ARTIST IN ('Beyoncé', 'Kendrick Lamar');

-- SQL LIKE
SELECT
	YEAR,
	CATEGORY
FROM
	GRAMMY_WINNERS
WHERE
	CATEGORY LIKE '%Dance%';

